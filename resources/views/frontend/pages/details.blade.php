         <h4>Challenge</h4>
         <p>Crime, teenage pregnancy, poverty, HIV, unemployment and hopelessness are the main challenges confronting children in Greater Tzaneen. We work with 87 volunteers in 78 villages helping children to build resilience to over come often horrendous life situations and build on those success&#39;s so that the children will have the emotional, physical and spiritual strength to enter adulthood as leaders helping others to achieve their dreams.</p>

         <h4>Solution</h4>
         <p>We use the Scouts model of cascade training and peer support to empower the children to make wise decisions about their lives from a value&#39;s based perspective. We have a code of conduct which the children agree to in order to participate in the project. The children then are involved in many different activities which are age and skills appropriate as they develop as young leaders. A feature of our project is that each group must be involved in a community project to give back to their village.</p>

         <h4>Long-Term Impact</h4>
         <p>In 16 yrs we have worked with an excess of 12,000 children.  - Teenage pregnancy has reduced from 13%provincially to 0.07% amongst our program.  - Matric Pass rate of 90% governments pass rate is 62%  - We have already produced doctors, lawyers, social workers, physio&#39;s, occupational therapists, teachers, nurses, mechanics, fitters, electricians etc  The children have hope for a future and the skills to realize their dreams. We are changing South Africa - one child at a time!</p>

